#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]
pub struct Vec2i {
    pub x: i32,
    pub y: i32,
}

#[derive(Debug, Clone, Copy, PartialEq, Default)]
pub struct Vec2f {
    pub x: f32,
    pub y: f32,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]
pub struct Vec2u {
    pub x: u32,
    pub y: u32,
}
